/**
 * @param {string} chartKey - e.g. "20CHT"
 * @param {string|number} digitKey - 0–9
 * @returns {string[]} 3-digit strings (zero-padded) for that chart column
 */
export function getNumbersForChartDigit(chartKey, digitKey) {
    const chart = chartData[chartKey];
    if (!chart) return [];
    const d = Number(digitKey);
    if (!Number.isInteger(d) || d < 0 || d > 9) return [];
    const list = chart[d];
    if (!Array.isArray(list)) return [];
    return list.map((n) => String(Number(n)).padStart(3, '0'));
}
